    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
//aqui
            String nombre = this.txtNombre.getText();
            String direccion= this.txtDireccion.getText();
            String NIT = this.txtNIT.getText();
            String telefono = this.txtTelefono.getText();
if (        !"".equals(nombre.trim()) && !"".equals(direccion.trim()) && !"".equals(NIT.trim()) && !"".equals(telefono.trim())){
        try {
            cn.conectarBD();


            Statement stmt = cn.conn.createStatement() ;

            String query = "INSERT INTO proveedor (proveedor_nombre, proveedor_nit, proveedor_direccion, "
            + "proveedor_telefono) VALUES(?,?,?,?);" ;
            PreparedStatement pst  = cn.conn.prepareStatement(query);
            pst.setString(1, nombre);
            pst.setString(2, NIT);
            pst.setString(3, direccion);
            pst.setString(4, telefono);

            int n=pst.executeUpdate();

            // String query2 = "INSERT INTO registro (,empleado_code)";
            JOptionPane.showMessageDialog(null, "Proveedor Guardado");
            this.txtNIT.setText("");
            this.txtNombre.setText("");
            this.txtDireccion.setText("");
            this.txtTelefono.setText("");
cn.conn.close();
        } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null,"Los datos no tienen el formato correcto", "Mensaje",JOptionPane.WARNING_MESSAGE);
        }
}else {
JOptionPane.showMessageDialog(null, "Llene todos los campos");
}

    }//GEN-LAST:event_btnAceptarActionPerformed

